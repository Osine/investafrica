    <h1>About the Metrics Used</h1>
<h2>Institution</h2>
<p>The Institutions pillar of the Global Competitiveness Index 4.0 assesses security, property rights, social capital, checks and balances, transparency and ethics, public-sector performance, future orientation of government, and corporate governance. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Infrastructure</h2>
<p>The Infrastructure pillar of the Global Competitiveness Index 4.0 assesses the quality and extension of transport infrastructure (road, rail, water and air) and utility infrastructure (electricity and water). See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>ICT Adoption</h2>
<p>The ICT adoption pillar of the Global Competitiveness Index 4.0 assesses the degree of diffusion of specific information and communication technologies (ICTs). See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Macroeconomic stability</h2>
<p>The Macroeconomic stability pillar of the Global Competitiveness Index 4.0 assesses the level of inflation and the sustainability of fiscal policy (see Box 2, Chapter 3 of The Global Competitiveness Report 2018 for further explanation). See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Health</h2>
<p>The Health pillar of the Global Competitiveness Index 4.0 assesses health-adjusted life expectancy (HALE)—the average number of years a newborn can expect to live in good health. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Skills</h2>
<p>The Skills pillar of the Global Competitiveness Index 4.0 assesses the general level of skills of the workforce and the quantity and quality of education. While the concept of educational quality is constantly evolving, important quality factors today include: developing digital literacy, interpersonal skills, and the ability to think critically and creatively. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Product Market</h2>
<p>The Product market pillar of the Global Competitiveness Index 4.0 assesses the extent to which a country provides an even playing field for companies to participate in its markets. It is measured in terms of extent of market power, openness to foreign firms and the degree of market distortions. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Labour Market</h2>
<p>The Labour market pillar of the Global Competitiveness Index 4.0 assesses the flexibility of the labor market, namely, the extent to which human resources can be reorganized and talent management”, namely, the extent to which human resources are leveraged. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Financial System</h2>
<p>The Financial system pillar of the Global Competitiveness Index 4.0 assesses the depth, namely the availability of credit, equity, debt, insurance and other financial products, and the stability, namely, the mitigation of excessive risk-taking and opportunistic behavior of the financial system. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Market Size</h2>
<p>The Market size pillar of the Global Competitiveness Index 4.0 assesses the size of the domestic and foreign markets to which a country’s firms have access. It is proxied by the sum of the value of consumption, investment and exports. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Business Dynamism</h2>
<p>The Business dynamism pillar of the Global Competitiveness Index 4.0 assesses the private sector’s capacity to generate and adopt new technologies and new ways to organize work, through a culture that embraces change, risk, new business models, and administrative rules that allow firms to enter and exit the market easily. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
<h2>Innovation Capacity</h2>
<p>The Innovation capability pillar of the Global Competitiveness Index 4.0 assesses the quantity and quality of formal research and development; the extent to which a country’s environment encourages collaboration, connectivity, creativity, diversity and confrontation across different visions and angles; and the capacity to turn ideas into new goods and services. See Appendix A of The Global Competitiveness Report 2019 for details.</p>
